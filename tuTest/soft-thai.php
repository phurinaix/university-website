						</div>
						<div id="detail">
							<p>ชื่อสถาบันอุดมศึกษา : มหาวิทยาลัยธรรมศาสตร์</p><br>
							<p>วิทยาเขต/คณะ/ภาควิชา  :  ศูนย์รังสิต และศูนย์พัทยา คณะวิศวกรรมศาสตร์ </p><br>
							<p>1. ชื่อหลักสูตร</p><br>
							<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ภาษาไทย : หลักสูตรวิศวกรรมศาสตรบัณฑิต สาขาวิชาวิศวกรรมซอฟต์แวร์</p><br>
							<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ภาษาอังกฤษ : Bachelor of Engineering Program in Software Engineering</p><br>
							<p>2. ชื่อปริญญาและสาขาวิชา</p><br>
							<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspภาษาไทย : ชื่อเต็ม วิศวกรรมศาสตรบัณฑิต (วิศวกรรมซอฟต์แวร์)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ชื่อย่อ วศ.บ. (วิศวกรรมซอฟต์แวร์)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp ภาษาอังกฤษ : ชื่อเต็ม Bachelor of Engineering (Software Engineering)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspชื่อย่อ B. Eng. (Software Engineering)</p><br>
	                        <p>3. วิชาเอก : -</p><br>
	                        <p>4. จำนวนหน่วยกิตที่เรียนตลอดหลักสูตร</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspจำนวนหน่วยกิตตลอดหลักสูตร 133 หน่วยกิต</p><br>
	                        <p>5. รูปแบบของหลักสูตร</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5.1 รูปแบบ : หลักสูตรระดับปริญญาตรีหลักสูตร 4 ปี</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5.2 ภาษาที่ใช้ : หลักสูตรจัดการศึกษาเป็นภาษาไทย</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5.3 การรับเข้าศึกษา : รับทั้งนักศึกษาไทยและนักศึกษาต่างชาติที่สามารถใช้ภาษาไทยได้เป็นอย่างดี</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5.4 ความร่วมมือกับสถาบันอื่น : เป็นหลักสูตรของสถาบันโดยเฉพาะ</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5.5 การให้ปริญญาแก่ผู้สำเร็จการศึกษา : ให้ปริญญาเพียงสาขาวิชาเดียว</p><br>
	                        <p>6. อาชีพที่สามารถประกอบได้หลังสำเร็จการศึกษา</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.1 วิศวกรซอฟต์แวร์ (Software Engineer) หรือนักเขียนโปรแกรม (Programmer/Developer)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.2 นักวิเคราะห์ระบบหรือนักออกแบบระบบ (System Analyst / Designer)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.3 วิศวกรความต้องการ (Requirement Engineer)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.4 นักประกันคุณภาพซอฟต์แวร์ (Software Quality Assurance)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.5 วิศวกรปรับปรุงกระบวนการซอฟต์แวร์ (Software Process Improvement Engineer)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.6 นักทดสอบระบบ (Software Tester)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.7 สถาปนิกซอฟต์แวร์ (Software Architect)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.8 นักบูรณาการระบบ (System Integrator)</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6.9 ผู้จัดการโครงการซอฟต์แวร์ (Software Project Manager)</p><br>
	                        <p>7. สถานที่จัดการเรียนการสอน คณะวิศวกรรมศาสตร์ มหาวิทยาลัยธรรมศาสตร์</p><br>
	                        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspเรียนที่คณะวิศวกรรมศาสตร์ มหาวิทยาลัยธรรมศาสตร์ ศูนย์รังสิต สำหรับชั้นปีที่ 1 และเรียนที่ศูนย์พัทยาตั้งแต่ชั้นปีที่ 2 จนจบหลักสูตร</p><br>
